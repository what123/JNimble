package com.jnimble.sdk.route;

/**
 * HTTP methods supported by the minimal plugin route protocol.
 */
public enum RouteMethod {
    /** HTTP GET request. */
    GET,
    /** HTTP POST request. */
    POST,
    /** HTTP PUT request. */
    PUT,
    /** HTTP PATCH request. */
    PATCH,
    /** HTTP DELETE request. */
    DELETE
}
